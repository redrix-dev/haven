import crypto from 'node:crypto';
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..', '..', '..');

const appAssetsJsonPath = path.resolve(
  repoRoot,
  'packages/shared/src/config/appAssets.json'
);
const appAssets = JSON.parse(await fs.readFile(appAssetsJsonPath, 'utf8'));

const publicDir = path.resolve(repoRoot, 'apps/web-mobile/public');
const generatedTsDir = path.resolve(repoRoot, 'apps/web-mobile/src/generated');
const indexTemplatePath = path.resolve(
  repoRoot,
  'apps/web-mobile/src/index.template.html'
);
const indexHtmlPath = path.resolve(repoRoot, 'apps/web-mobile/src/index.html');

const write = async (filePath, content) => {
  await fs.mkdir(path.dirname(filePath), { recursive: true });
  await fs.writeFile(filePath, content, 'utf8');
};

const normalizeAssetPublicPath = (assetPath) => {
  if (typeof assetPath !== 'string' || assetPath.trim().length === 0) {
    throw new Error(`Invalid web asset path: ${String(assetPath)}`);
  }

  const trimmed = assetPath.trim();
  return trimmed.startsWith('/') ? trimmed : `/${trimmed}`;
};

const publicFilePathFromAssetPath = (assetPath) =>
  path.resolve(publicDir, normalizeAssetPublicPath(assetPath).replace(/^\//, ''));

const createVersionedAssetUrl = async (assetPath) => {
  const normalizedPath = normalizeAssetPublicPath(assetPath);
  const assetFilePath = publicFilePathFromAssetPath(normalizedPath);
  let contents;

  try {
    contents = await fs.readFile(assetFilePath);
  } catch (error) {
    throw new Error(
      `Missing configured web asset file: ${assetFilePath} (${error instanceof Error ? error.message : String(error)})`
    );
  }

  const version = crypto
    .createHash('sha256')
    .update(contents)
    .digest('hex')
    .slice(0, 10);
  return `${normalizedPath}?v=${version}`;
};

const rawWebAssets = {
  pwaIcon192: appAssets.web.pwaIcons.size192,
  pwaIcon512: appAssets.web.pwaIcons.size512,
  browserTabIcon: appAssets.web.browserTabIcon ?? appAssets.web.pwaIcons.size192,
  appleTouchIcon: appAssets.web.appleTouchIcon ?? appAssets.web.pwaIcons.size192,
  splashIcon: appAssets.web.splashIcon,
  notificationIconFallback: appAssets.web.notification.iconFallback,
  notificationBadgeFallback: appAssets.web.notification.badgeFallback,
};

const webAssetsEntries = await Promise.all(
  Object.entries(rawWebAssets).map(async ([key, assetPath]) => [
    key,
    await createVersionedAssetUrl(assetPath),
  ])
);
const webAssets = Object.fromEntries(webAssetsEntries);

const manifestContents = `${JSON.stringify(
  {
    id: '/',
    name: 'Haven',
    short_name: 'Haven',
    description: 'Community chat for people who give a damn',
    start_url: '/',
    scope: '/',
    display: 'standalone',
    background_color: '#0f1728',
    theme_color: '#3f79d8',
    icons: [
      {
        src: webAssets.pwaIcon192,
        sizes: '192x192',
        type: 'image/png',
        purpose: 'any',
      },
      {
        src: webAssets.pwaIcon512,
        sizes: '512x512',
        type: 'image/png',
        purpose: 'any',
      },
    ],
  },
  null,
  2
)}\n`;

await write(path.resolve(publicDir, 'manifest.json'), manifestContents);

const manifestUrl =
  '/manifest.json?v=' +
  crypto
    .createHash('sha256')
    .update(manifestContents)
    .digest('hex')
    .slice(0, 10);

const webRuntimeAssets = {
  ...webAssets,
  manifestUrl,
};

await write(
  path.resolve(publicDir, 'app-assets.generated.json'),
  `${JSON.stringify(webRuntimeAssets, null, 2)}\n`
);

await write(
  path.resolve(publicDir, 'app-assets.generated.js'),
  `self.HAVEN_APP_ASSETS = ${JSON.stringify(webRuntimeAssets, null, 2)};\n`
);

await write(
  path.resolve(generatedTsDir, 'appAssets.generated.ts'),
  `// AUTO-GENERATED by tooling/scripts/assets/generate-app-assets.mjs\n` +
    `export const webAppAssets = ${JSON.stringify(
      webRuntimeAssets,
      null,
      2
    )} as const;\n`
);

const indexTemplate = await fs.readFile(indexTemplatePath, 'utf8');
const generatedHeadMarkup = [
  `    <link rel="manifest" href="${webRuntimeAssets.manifestUrl}" />`,
  '    <meta name="theme-color" content="#3f79d8" />',
  '    <meta name="mobile-web-app-capable" content="yes" />',
  '    <meta name="apple-mobile-web-app-capable" content="yes" />',
  '    <meta name="apple-mobile-web-app-status-bar-style" content="black-translucent" />',
  '    <meta name="apple-mobile-web-app-title" content="Haven" />',
  '    <meta name="application-name" content="Haven" />',
  `    <link rel="icon" type="image/png" sizes="192x192" href="${webRuntimeAssets.browserTabIcon}" />`,
  `    <link rel="icon" type="image/png" sizes="512x512" href="${webRuntimeAssets.pwaIcon512}" />`,
  `    <link rel="shortcut icon" href="${webRuntimeAssets.browserTabIcon}" />`,
  `    <link rel="apple-touch-icon" href="${webRuntimeAssets.appleTouchIcon}" />`,
].join('\n');

if (!indexTemplate.includes('<!-- HAVEN_GENERATED_WEB_APP_HEAD -->')) {
  throw new Error(
    `Missing <!-- HAVEN_GENERATED_WEB_APP_HEAD --> marker in ${indexTemplatePath}`
  );
}

await write(
  indexHtmlPath,
  indexTemplate.replace(
    '    <!-- HAVEN_GENERATED_WEB_APP_HEAD -->',
    generatedHeadMarkup
  )
);

console.log('Generated web asset manifests, runtime constants, and head icon wiring.');
